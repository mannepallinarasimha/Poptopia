package com.kelloggs.promotions.lib.model;

public class RewardReport {

    private String promotionName;
    private String region;
    private String locale;
    private Integer codeUsed;
    private Integer maxCodeQty;
    private Double percentageUsed;
    private Boolean sendAlert;

    public RewardReport() {
        //default Constructor
    }

    public RewardReport(String promotionName, String region, String locale, Integer codeUsed, Integer maxCodeQty, Double percentage, Boolean sendAlert) {
        this.promotionName = promotionName;
        this.region = region;
        this.locale = locale;
        this.codeUsed = codeUsed;
        this.maxCodeQty = maxCodeQty;
        this.percentageUsed = percentage;
        this.sendAlert = sendAlert;
    }

    public String getPromotionName() {
        return promotionName;
    }

    public void setPromotionName(String promotionName) {
        this.promotionName = promotionName;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getLocale() {
        return locale;
    }

    public void setLocale(String locale) {
        this.locale = locale;
    }

    public Integer getCodeUsed() {
        return codeUsed;
    }

    public void setCodeUsed(Integer codeUsed) {
        this.codeUsed = codeUsed;
    }

    public Integer getMaxCodeQty() {
        return maxCodeQty;
    }

    public void setMaxCodeQty(Integer maxCodeQty) {
        this.maxCodeQty = maxCodeQty;
    }

    public Double getPercentageUsed() {
        return percentageUsed;
    }

    public void setPercentageUsed(Double percentageUsed) {
        this.percentageUsed = percentageUsed;
    }

    public Boolean getSendAlert() {
        return sendAlert;
    }

    public void setSendAlert(Boolean sendAlert) {
        this.sendAlert = sendAlert;
    }
}
