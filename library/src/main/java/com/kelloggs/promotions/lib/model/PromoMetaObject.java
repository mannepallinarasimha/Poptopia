package com.kelloggs.promotions.lib.model;

import java.util.List;

public class PromoMetaObject {
	
	private String winSelectionType;//winmoment,sweepstake
	private String winMechanism;//instantwin,luckywinner
	
	private String winFrequency;
	private Integer winnerCount;   //for sweepstake
	private List<WinDateConfig> winDateConfig; //for win moment
	private String rewardstep;
	
	public PromoMetaObject() {
		//default constructor
	}

	public String getWinSelectionType() {
		return winSelectionType;
	}



	public void setWinSelectionType(String winSelectionType) {
		this.winSelectionType = winSelectionType;
	}



	public String getWinMechanism() {
		return winMechanism;
	}



	public void setWinMechanism(String winMechanism) {
		this.winMechanism = winMechanism;
	}



	public List<WinDateConfig> getWinDateConfig() {
		return winDateConfig;
	}



	public void setWinDateConfig(List<WinDateConfig> winDateConfig) {
		this.winDateConfig = winDateConfig;
	}



	public String getWinFrequency() {
		return winFrequency;
	}



	public void setWinFrequency(String winFrequency) {
		this.winFrequency = winFrequency;
	}



	public Integer getWinnerCount() {
		return winnerCount;
	}



	public void setWinnerCount(Integer winnerCount) {
		this.winnerCount = winnerCount;
	}

	public String getRewardstep() {
		return rewardstep;
	}

	public void setRewardstep(String rewardstep) {
		this.rewardstep = rewardstep;
	}
	
	
	
}
